제 6조 (이용자의 권리, 의무 및 행사 방법)
1. 이용자는 회사에 대하여 언제든지 다음 각 호의 개인정보 보호 관련 권리를 행사할 수 있습니다.
​
1) 개인정보의 처리에 관한 정보를 제공받을 권리
2) 개인정보의 처리에 관한 동의 여부, 동의 범위 등을 선택하고 결정할 권리
3) 개인정보의 처리 여부를 확인하고 개인정보에 대한 열람(사본 발급을 포함)을 요구할 권리
4) 개인정보의 처리 정지, 정정·삭제 및 파기를 요구할 권리
5) 개인정보의 처리로 인하여 발생한 피해를 신속하고 공정한 절차에 따라 구제받을 권리
​
2. 제1항에 따른 권리 행사는 회사에 대해 서면(전자문서 또는 전자우편을 포함), 모사전송(FAX) 등을 통하여 하실 수 있으며 회사는 이에 대해 지체 없이 조치하겠습니다. 다만, 관련 법령에서 개인정보가 수집 대상으로 명시되어 있는 경우 회사는 이용자에게 삭제할 수 없음을 알려 드립니다.
3. 이용자가 개인정보의 오류 등에 대한 정정을 요청한 경우에 회사는 정정 또는 삭제를 완료할 때까지 당해 개인정보를 이용하거나 제공하지 않습니다.
4. 이용자는 제1항에 따른 권리 행사를 이용자의 법정대리인이나 위임을 받은 자 등 대리인을 통하여 하실 수 있으며, 이 경우 이용자는 회사에 대하여 개인정보 보호법 시행규칙 별지 제11호 서식에 따른 위임장을 제출하셔야 합니다.
5. 이용자는 고유식별정보 등 개인정보의 관리 책임을 부담하며, 타인의 개인정보를 도용하거나 사용권한을 넘어 사용하여서는 아니되며, 이로 인하여 발생되는 일체의 법적 책임은 이용자가 부담합니다.
6. 이용자는 서비스의 이용과 관련하여 타인의 개인정보를 훼손하여서는 아니됩니다. 만일 이용자가 회사의 서비스와 직·간접적인 영역(온라인 또는 오프라인)에서 타인의 개인정보를 훼손할 경우 그 훼손으로 인하여 회사 및 이용자에게 발생하는 일체의 유·무형적 손해에 대하여 법적 책임을 부담합니다.


제 7조(개인정보의 파기)
