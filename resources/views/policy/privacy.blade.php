- 회원자격 유지·관리
- 제한적 본인확인제 시행에 따른 본인확인
- 서비스 부정이용 방지 등
2. 재화 또는 서비스 제공
- 상담 서비스 제공
- 콘텐츠 제공
- 용역 서비스 제공
- 맞춤 서비스 제공
- 본인인증 등 목적
3. 마케팅 및 서비스 품질 개선 등
- 회사의 마케팅, 서비스 품질에 대한 후기(Review)게시, 서비스 개선 및 처리결과 통보 등의 목적
4. 이벤트
​- 이벤트 기간 내 변동사항 고지 또는 기간 임박에 대한 안내
기타 위 수집된 정보를 통해 서비스 제공에 관한 계약 이행 및 요금 정산, 회원관리 등에 활용하고 있습니다

