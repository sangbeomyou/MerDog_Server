제 5조(개인정보의 제3자 제공 및 목적 외 이용의 제한)
1. 펫닥의 수의사 상담 서비스 이용 시 수의사에게 회원의 이름, 닉네임, 반려동물 나이, 반려동물 성별, 반려동물 종류, 반려동물 품종, 접종 여부, 중성화 여부, 지난 상담내역, 위치정보가 제공됩니다.
2. 펫닥의 장례 서비스 이용 시 장례서비스 제공 업체에게 회원님의 이름, 위치정보가 제공됩니다.
3. 펫닥은 이용자의 개인정보를 제3자(개인, 법인, 국가, 지방자치단체,기관 등)에 제공하거나 또는 제1조의 개인정보 처리목적을 초과하여 이용하지 않습니다. 다만 회사는 서비스 품질 개선 및 향상 등의 목적을 위해 이용자의 개인정보를 제3자와 제공(공유를 포함)할 필요가 있는 경우에는 사전에 이용자에게 개인정보를 제공받는 자, 개인정보를 제공받는 자의 개인정보 이용 목적. 제공하는 개인정보의 항목. 개인정보를 제공받는 자의 개인정보 보유 및 이용 기간, 동의를 거부할 권리가 있다는 사실 및 동의 거부에 따른 불이익이 있는 경우에는 그 불이익 내용을 서면(전자문서 또는 전자우편을 포함)에 의하여 알리고 동의를 얻을 것이며, 이용자가 제3자에 대한 개인정보 제공에 동의하지 않을 경우에는 개인정보를 제 3자에게 제공하지 않습니다.
4. 제 3항에도 불구하고 다음의 경우에는 이용자 또는 제3자의 이익을 부당하게 침해할 우려가 있을 때를 제외하고는 이용자의 개인정보를 이용자의 동의 없이 제3자에게 제공하거나 목적 외의 용도로 이용할 수 있습니다.
1) 법률에 특별한 규정이 있거나 법령상 의무를 준수하기 위하여 불가피한 경우
2) 공공기관이 법령 등에서 정하는 소관 업무의 수행을 위하여 불가피한 경우
3) 이용자 또는 그 법정 대리인이 의사표시를 할 수 없는 상태에 있거나 주소불명 등으로 사전 동의를 받을 수 업는 경우로서 명백히 이용자 또는 제3자의 급박한 생명, 신체, 재산의 이익을 위하여 필요하다고 인정되는 경우
4) 통계작성 및 학술연구 등의 목적을 위하여 필요한 경우로서 특정 개인을 알아볼 수 없는 형태로 개인정보를 제공하는 경우
5) 개인정보를 목적 외의 용도로 이용하거나 이를 제3자에게 제공하지 아니하면 다른 법률에서 정하는 소관업무를 수행할 수 없는 경우로서 보호위원회의 심의/의결을 거친 경우
6) 조약, 그 밖의 국제협정의 이행을 위하여 외국정부 또는 국제기구에 제공하기 위하여 필요한 경우
7) 범죄의 수사와 공소의 제기 및 유지를 위하여 필요한 경우
8) 법원의 재판업무 수행을 위하여 필요한 경우
9) 형(刑) 및 감호, 보호처분의 집행을 위하여 필요한 경우
5. 펫닥은 이용자의 별도 동의를 얻어 다음과 같이 개인정보를 제3자에게 제공하고 있습니다.
