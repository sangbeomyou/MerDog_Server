펫닥 개인정보 이용 약관

(주)펫닥과 VLAB 등의 패밀리 사이트(이하 합쳐서 “펫닥”또는 “회사”라고 한다)은 개인정보보호법에 따라 이용자의 개인정보 보호 및 권익을 보호하고 개인정보와 관련한 이용자의 고충을 원활하게 처리할 수 있도록 최선을 다하고 있습니다. 펫닥은 통신비밀보호법, 전기통신사업법, 정보통신망 이용촉진 및 정보보호 등에 관한 법률 등 정보통신서비스제공자가 준수하여야 할 관련 법규상의 개인정보보호 규정 및 정보통신부가 제정한 개인정보보호지침을 준수하고 있습니다. V+LAB(주)는 개인정보 처리방침을 통하여 이용자들이 제공하는 개인정보가 어떠한 용도와 방식으로 이용되고 있으며 개인정보보호를 위해 어떠한 조치가 취해지고 있는지 알려 드립니다
 펫닥은 관계법령, 정부의 지침 및 펫닥의 정책 변경 등에 따라 개인정보취급방침을 개정할 수 있고, 개정하는 경우 웹사이트 공지사항(또는 개별공지)을 통하여 공지하도록 하겠습니다.
펫닥의 개인정보 처리방침은 정부의 법률 및 지침 변경이나 회사의 내부 방침 변경 등으로 인하여 수시로 변경될 수 있고, 이에 따른 개인정보 처리방침의 지속적인 개선을 위하여 필요한 절차를 정하고 있습니다. 그리고 개인정보 처리방침을 개정하는 경우나 개인정보 처리방침 변경될 경우 서비스의 첫 페이지의 개인정보처리방침을 통해 고지하고 있습니다. 이용자들께서는 사이트 방문 시 수시로 확인하시기 바랍니다.

제 1조 (개인정보 수집 동의)
펫닥은 이용자들이 회사의 개인정보 처리방침 또는 이용약관의 내용에 대하여 「동의」버튼 또는 「취소」버튼을 클릭할 수 있는 절차를 마련하여, 「동의」버튼을 클릭하면 펫닥과 VLAB 통합 사이트의 개인정보 수집에 대해 동의한 것으로 봅니다.
​
제 2조 (개인정보의 수집·이용 목적)
다음의 목적을 위하여 개인정보를 수집 및 이용합니다.
수집 · 이용하고 있는 개인정보는 다음의 목적 이외의 용도로는 이용되지 않으며, 이용 목적이 변경되는 경우에는 별도 안내 후 동의를 받는 등 필요한 조치를 이행할 예정입니다.

1. 웹/앱 회원가입 및 관리
- 회원 가입의사 확인
